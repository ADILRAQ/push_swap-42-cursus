#include "../header.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*ptr;

	if (!lst || !new)
		return ;
	ptr = *lst;
	if (*lst)
	{
		ptr = ft_lstlast(ptr);
		ptr->next = new;
		new->prev = (*lst);
	}
	else
		*lst = new;
}
